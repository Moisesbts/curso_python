print(1234)

# Aspas simples
print('Allan Moisés')

# Aspas Duplas
print("Allan Nunes")

# Escape - (Barra invertida é considerada um caracter de escape (ele não interpreta o caracter seguinte de onde é colocado))
print("Allan \"Moisés\"")

# r - Exibe os caracteres especiais
print(r"Allan \"Moisés\" Nunes")

# Inverter aspas - não há diferença entre usar primeiro uma e depois a outra.
print('Allan "Moisés" Nunes')